import { uiActions } from "./ui-slice";
import { cartActions } from "./cart-slice";

export const fetchCartData = () => {
  return async (dispatch) => {
    const fetchData = async () => {
      const response = await fetch(
        "https://react-http-2668b-default-rtdb.firebaseio.com/cart.json"
      );
      if (!response.ok) {
        throw new Error("There is an error during sending data");
      }
      const data = await response.json();
      return data;
    };
    try {
      const cartData = await fetchData();
      dispatch(
        cartActions.replaceCart({
          items: cartData.items || [],
          totalQuantity: cartData.totalQuantity,
        })
      );
    } catch (error) {
      dispatch(
        uiActions.showNotification({
          status: "error",
          title: "Error!",
          message: "Fetching cart data failed!",
        })
      );
    }
  };
};

//Dispatchlenen herhangi bir method(function) içerisinde asynchronous Http request and postlama işleri yapma yöntemi
//Asynchronous veya useEffect kullanarak aynı zamanda reducers içerisindeki bilgileri kullanma yöntemi
//Dispatch içerisinde bu fonksyonu çalıştırabiliyosun
export const sendCartData = (cart) => {
  return async (dispatch) => {
    dispatch(
      uiActions.showNotification({
        status: "pending",
        title: "Sending...",
        message: "Sending cart data!",
      })
    );

    const sendRequest = async () => {
      const response = await fetch(
        "https://react-http-2668b-default-rtdb.firebaseio.com/cart.json",
        {
          method: "PUT", // POST yerine PUT kullandık çünkü post sürekli yeni bilgi eklemeye yararken PUT eskisinin üstüne overwrite yapıyo.
          body: JSON.stringify({
            items: cart.items,
            totalQuantity: cart.totalQuantity,
          }),
        }
      );

      if (!response.ok) {
        throw new Error("Sending cart data failed!");
      }
    };

    try {
      await sendRequest();
      dispatch(
        uiActions.showNotification({
          status: "success",
          title: "Success!",
          message: "Sent cart data successfully!",
        })
      );
    } catch (error) {
      dispatch(
        uiActions.showNotification({
          status: "error",
          title: "Error!",
          message: "Sending cart data failed!",
        })
      );
    }
  };
};
